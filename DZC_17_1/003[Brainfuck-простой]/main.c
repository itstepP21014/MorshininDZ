#include <stdio.h>
#include <stdlib.h>
#include <string.h>

int main()
{
    const int N=1500;//размер памяти
    //программа содержащая строку <Hello, world!> на Brainfuck
    char mas[]="++++++++++++++++++++++++++++++++++++++++>+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++>++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++>++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++>+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++>>+++++++++++++++++++++++++++++++++++++++++++++++++++++++>+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++>++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++>++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++>++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++>+.";
    char arr[N];//наша память, которая и будет интерпретировать различные команды
    for(int i=0; i<strlen(mas); i++)
    {
        arr[i]=' ';   // обнуляем память
    }
    int k=0;
    for(int i=0; i<strlen(mas); i++)
    {
        // интерпретация команд
        switch(mas[i])
        {
        case '>':
            k++;
            break;
        case '<':
            k--;
            break;
        case '+':
            arr[k]++;
            break;
        case '-':
            arr[k]--;
            break;
        case '.':
            for(int j=0; j<strlen(mas); j++)printf("%c",arr[j]);
            break;
        }
    }
    return 0;
}
